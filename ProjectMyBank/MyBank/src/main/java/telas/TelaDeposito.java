    private void btnDepositarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDepositarActionPerformed
        String textvalor = txtValor.getText();
        String textValorCerto = textvalor.replace(',', '.');
        try{
            double deposito = Double.parseDouble(textValorCerto);        
            double valor = deposito;        
            String chave = " ";
            TelaDigiteSenha tds = new TelaDigiteSenha(logado,valor,1,logado.getCodConta(),"Sem descrição",logado.getSaldo(),chave);
            tds.setVisible(true);        
            int id = 1;
            if(confirmacao){                    
                LocalDate dataAtual = LocalDate.now();
                String data = dataAtual.toString();
                DAO dao = new DAO();
                try {
                    double novoSaldo = logado.getSaldo() + deposito;
                    logado.setSaldo(novoSaldo);
                    ArrayList listaId = dao.todasTransferencias();
                    while(listaId.contains(id)){
                        id++;
                    }
                    dao.depositar(id,deposito,data, logado,novoSaldo);

                } catch (Exception ex) {
                    Logger.getLogger(TelaDeposito.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
        catch(Exception e){
            e.printStackTrace();
            int tipo = 12;
            TelaAvisos ta = new TelaAvisos(tipo);
            ta.setVisible(true);
        }
            this.confirmacao = false;
        
    }//GEN-LAST:event_btnDepositarActionPerformed

