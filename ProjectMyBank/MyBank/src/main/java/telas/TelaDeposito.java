    public TelaDeposito(boolean confirmacao,User logado,double valor) {
        super("Depósito");
        this.logado = logado;
        this.confirmacao = confirmacao;
        initComponents();
        setLocationRelativeTo(null);
        btnVoltar.setBackground(new Color(0,0,0,0));
        btnDepositar.setBackground(new Color(0,0,0,0));
        txtValor.setBackground(new Color(0,0,0,0));
        int id = 1;
        if(confirmacao){                    
            LocalDate dataAtual = LocalDate.now();
            String data = dataAtual.toString();
            DAO dao = new DAO();
            try {
                double novoSaldo = logado.getSaldo() + valor;
                logado.setSaldo(novoSaldo);
                ArrayList listaId = dao.todasTransferencias();
                while(listaId.contains(id)){
                    id++;
                }
                dao.depositar(id,valor,data, logado,novoSaldo);
            } catch (Exception ex) {
                Logger.getLogger(TelaDeposito.class.getName()).log(Level.SEVERE, null, ex);
                
            }
            this.confirmacao = false;
            
        }//txtValor.setText(Double.toString(logado.getSaldo()));
        
    }

