     public TelaPix(boolean confirmacao,User logado,double valor,int destinatario,String descricao,double saldofinaldest,String chave) {
        super("Depósito");
        this.logado = logado;
        this.confirmacao = confirmacao;
        initComponents();
        this.logado = logado;
        btnVoltar.setBackground(new Color(0,0,0,0));
        btnEnviar.setBackground(new Color(0,0,0,0));
        txtPix.setBackground(new Color(0,0,0,0));
        txtValor.setBackground(new Color(0,0,0,0));
        txtDesc.setBackground(new Color(0,0,0,0));
        setLocationRelativeTo(null);
        DAO dao = new DAO();
        int id = 1;      
        if(confirmacao){
                LocalDate dataAtual = LocalDate.now();
                String data = dataAtual.toString();
                
                try {
                    double novoSaldo = logado.getSaldo() - valor;
                    logado.setSaldo(novoSaldo);
                    ArrayList listaId = dao.todasTransferencias();
                    while(listaId.contains(id)){
                        id++;
                    }
                    dao.fazerPix(id,valor,data, logado,novoSaldo,destinatario,descricao,saldofinaldest,chave);                   
                } catch (Exception ex) {
                    Logger.getLogger(TelaDeposito.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
            this.confirmacao = false;
        
    }

