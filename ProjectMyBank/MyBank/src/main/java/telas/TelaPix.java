    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        try {
            String chave = txtPix.getText();
            DAO dao = new DAO();
            ArrayList todasChaves = dao.todasChavesPix();
            if(todasChaves.contains(chave)){
                String textvalor = txtValor.getText();
                String textValorCerto = textvalor.replace(',', '.');
                double pix = Double.parseDouble(textValorCerto);
                double valor = pix;            
                int destinatario = dao.verificaCodPix(chave);
                String descricao = txtDesc.getText();
                double saldodestinatario = dao.verificaSaldo(destinatario);
                double saldofinaldest = saldodestinatario + valor;
                TelaDigiteSenha tds = new TelaDigiteSenha(logado,valor,3,destinatario,descricao,saldofinaldest,chave);
                tds.setVisible(true);
                int id = 1;
                if(confirmacao){
                    LocalDate dataAtual = LocalDate.now();
                    String data = dataAtual.toString();

                    try {
                        double novoSaldo = logado.getSaldo() - valor;
                        logado.setSaldo(novoSaldo);
                        ArrayList listaId = dao.todasTransferencias();
                        while(listaId.contains(id)){
                            id++;
                        }
                        dao.fazerPix(id,valor,data, logado,novoSaldo,destinatario,descricao,saldofinaldest,chave);
                        JOptionPane.showMessageDialog(null,"Você fez um depósito de: \n R$ " + valor);
                    } catch (Exception ex) {
                        Logger.getLogger(TelaDeposito.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
            }
            else{
                int tipo = 13;
                TelaAvisos ta = new TelaAvisos(tipo);
                ta.setVisible(true);
            }
            this.confirmacao = false;            
        } catch (Exception ex) {
            Logger.getLogger(TelaTransferir.class.getName()).log(Level.SEVERE, null, ex);
            int tipo = 12;
            TelaAvisos ta = new TelaAvisos(tipo);
            ta.setVisible(true);
        }
        
    }//GEN-LAST:event_btnEnviarActionPerformed

